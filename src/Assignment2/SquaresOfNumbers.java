package Assignment2;

public class SquaresOfNumbers {
    public static void main(String[] args) {
        int n = 15;  // Change this to any number
        System.out.println("Squares of numbers from 1 to " + n + ":");
        for (int i = 1; i <= n; i++) {
            System.out.println(i + " square = " + (i * i));
        }
    }
}
